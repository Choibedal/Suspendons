        </p>
        <p>La dernière étude officielle date de 2012 et a été réalisée par l’INSEE, qui recensait à l’époque 141 500 personnes sans domicile en France métropolitaine. Ce chiffre représente une augmentation de 50% depuis 2001, et tout pense à croire que ce
            phénomène ne semble pas s’être amélioré depuis 2012.</p>
        <p>La difficulté de recensement vient du fait de l’absence de papiers de certains sans-domicile, de l’absence d’intégrations auprès de structures et/ou professionnels médicaux, et bien entendu de la difficulté à compter l’ensemble de cette population
            mobile et souvent cachée.
        </p>
        <p>
            Le profil de ces sans-abris et leurs moyens ont changé depuis les années 2000.
            </br>Philippe, SDF depuis une vingtaine d’année, confiait déjà en 2012 au journal Le Nouvel Observateur : « On s’imagine les SDF avec trois pièces en poche, assis à côté d’un litron de rouge mais c’est fini, ça. On a des comptes en banque
            et des téléphones », « Pour un SDF, le portable, c’est tout ! […] C’est comme un lien social, ça sert pas qu’à téléphoner. »
        </p>
        <p>Cette tendance, très rapidement confirmable auprès des premiers SDF croisés tout au long de notre étude, s’est accentuée, si bien que certaines associations estiment que 80% à 90% des SDF ont aujourd’hui un smartphone. En cause, la baisse des
            prix de ces derniers, et l’omniprésence des points de recharges et bornes wifi gratuites.
        </p>
