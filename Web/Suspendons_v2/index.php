            côté d’un litron de rouge mais c’est fini, ça. On a des comptes en banque et des téléphones », « Pour un SDF, le portable, c’est tout ! […] C’est comme un lien social, ça sert pas qu’à téléphoner. » Cette tendance, très rapidement confirmable
            auprès des premiers SDF croisés tout au long de notre étude, s’est accentuée, si bien que certaines associations estiment que 80% à 90% des SDF ont aujourd’hui un smartphone. En cause, la baisse des prix de ces derniers, et l’omniprésence
            des points de recharges et bornes wifi gratuites. En effet, le prix neuf des smartphones d’entrée de gamme est de 100 euros (pour des appareils atteignant 10 à 20 euros en occasion), et de nombreuses chaines de restaurations populaire, à l’image
            de Flunch, MacDonald’s ou encore Quick, proposent un wifi gratuitement et des prises dans leurs enseignes.</p>
