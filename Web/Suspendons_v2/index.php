            France métropolitaine. Ce chiffre représente une augmentation de 50% depuis 2001, et tout pense à croire que ce phénomène ne semble pas s’être amélioré depuis 2012. La difficulté de recensement vient du fait de l’absence de papiers de certains
            sans-domicile, de l’absence d’intégrations auprès de structures et/ou professionnels médicaux, et bien entendu de la difficulté à compter l’ensemble de cette population mobile et souvent cachée.</p>

