<section id="Suspen' Dons" class="four">
    <div class="container">
        <header>
            <h2>Suspen’ Dons : une plateforme pour tous</h2>
        </header>
        <p>De ce phénomène nous est venue une idée :
            <br>Comment pouvons-nous mettre à profit les nouvelles technologies dont disposent les citoyens français et les plus démunis dans l’optique de subvenir à leurs besoins ?
            <br><br>Nous avons eu donc l’idée de la plateforme « Suspen’Dons » : une plateforme disponible en ligne et sur mobile où l’utilisateur pourrait payer un service ou un produit de manière préventive, et non pas financer des associations caritatives
            qui manque parfois de transparence. On aurait ici un suivi clair et une description précise de l’utilisation de son don.</p>

        <!-- Services -->
        <p>La plateforme permet d’offrir, dans sa version initiale, différents produits identifiés comme primaires dans la vie des plus démunis : </p>
        <ul class="default">
            <li>Un repas</li>
            <li>Une douche</li>
            <li>Un service de rasage / coiffeur</li>
            <li>Un service de pressing / laverie automatique</li>
            <li>Location ou achat de vêtements propres en vue d'entretiens professionnels</li>
        </ul>

        <p>Ces services sont les premiers sur lesquels nous voudrions concentrer l’ensemble des fonds disponibles. D’autres produits seront bien sûr disponibles dès que l’offre le permettra.</p>

        <p>Le prix des produits sera négocié auprès de nos partenaires, afin de pouvoir offrir au plus grand nombre. Dans cette optique, les professionnels partenaires s’engageront donc à réduire au maximum leurs marges commerciales. Le gain, pour eux, est
            avant tout un gain d’image. Dans le contexte social et politique de la France actuelle, de plus en plus de personnes prennent conscience de la difficulté de vivre en tant que SDF, et être reconnu en tant que commerce responsable et volontaire,
            c’est être reconnu en tant que professionnel aux valeurs proches de ses clients.
        </p>

        <p>Une cagnotte différente sera attribuée par service et par région : cagnotte alimentaire « Alsace », cagnotte alimentaire « Lorraine », cagnotte hygiène du corps « Alsace », etc …
            <br>L’application repose sur deux méthodes de rémunération afin de faire grossir les cagnottes affectées à chaque service : La possibilité de faire des dons, mensualisés ou ponctuels, et la possibilité de regarder des vidéos publicitaires,
            afin de générer de l’argent sans en dépenser. L’argent des publicités viendrait donc renforcer les cagnottes déjà existantes.</p>

        <p>Afin de pouvoir récupérer son produit gratuit, le bénéficiaire devra présenter une carte RFID au commerçant. Cette carte, délivrée par les associations et professionnels de santé partenaires, servira d’identification et de « moyen de paiement
            », et permettra, outre le fait de donner au sans-abri ce sentiment d’échange carte
            <=> services, d’éviter les abus et fausses transactions.</p>

